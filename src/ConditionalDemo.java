public class ConditionalDemo {

	public static void main(String[] args) {

		int marks = 1;
		int pack = 10;

		if (marks > 10 && pack >= 50) {
			System.out.println("v. good");
		} else if (marks >= 5 && pack >= 3) {
			System.out.println("good");
		} else if (marks > 7) {
			System.out.println("avg");
		} 
		
		System.out.println("bye");

	}
}











